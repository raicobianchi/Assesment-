package InfiniteTerrainSystem;


public class Compass {
	int s;
	int w;
	int n;
	int e;
	
public Compass (int s, int w, int n, int e) {
	this.s = s;
	this.w = w;
	this.n = n;
	this.e = e;
	
}

public int getS() {
	return s;
}

public void setS(int s) {
	this.s = s;
}

public int getW() {
	return w;
}

public void setW(int w) {
	this.w = w;
}

public int getN() {
	return n;
}

public void setN(int n) {
	this.n = n;
}

public int getE() {
	return e;
}

public void setE(int e) {
	this.e = e;
}



}			